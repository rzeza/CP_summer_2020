public class Loops {
    public static void main(String[] arg) {
        for (int i=0; i<5; i++) {
            System.out.println("i= " + i);
        }

        for (int j=10; j>2; j--) {
            System.out.println("j=" + j);
        }
        for (int n=1;n<=5;n++){
            System.out.println("In row is " +n);
        }


    }

}
